<?php

namespace App\Http\Controllers;

use App\Models\Genre;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;
use App\Models\Utilities;
use Auth;
use Validator;
use Carbon\Carbon;
use Yajra\DataTables\Facades\DataTables;


class GenreController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $breadcrumbs = [
            ['link'=>"/",'name'=>"Home"],['link'=> route('genre.index'), 'name'=>"Genre"], ['name'=>"list of Genres"]
        ];
        if (request()->ajax()) {
            $genre = Genre::where('is_deleted', 0)->orderBy('updated_at', 'desc');
            return Datatables::eloquent($genre)
            ->addColumn('action', function(Genre $genre) {
                            $html = Utilities::actionDropdown([['route' => route('genre.edit', $genre->id), 'name' => 'Edit'], ['route' => route('genre.delete', $genre->id), 'name' => 'Delete']]);
                            return $html;
                        })
            ->rawColumns(['action'])
            ->make(true);
        }
        return view('content.genre.index', [
            'breadcrumbs' => $breadcrumbs,
        ]);
    }

